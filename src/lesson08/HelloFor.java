package lesson08;

public class HelloFor {
    public static void main(String[] args) {
        myWeek();

    }

    public static void myWeek() {
        int weekDays = 5;
        //beginning
        //conditions
        //
        for (int i = 1; i < weekDays; i++) {
            System.out.println("I wake up at 7 o'clock ");
            System.out.println("I have a breakfast");
            System.out.println("I go to work");
            System.out.println("I am at home");
            System.out.println("I speak with neighbors ");
            System.out.println("I have a evening ");
            System.out.println("I watch Netflix");
            System.out.println("I go to bed");
            System.out.println(i);
            System.out.println("I stand up at 5 o'clock morning ");

        }
    }
}
